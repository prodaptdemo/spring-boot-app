package com.prodapt.app.model;

public class AppModel {

	
	private String appid;
	
	private String appName;
	
	private String status;
	
	private String nooftimeinstalled;
	
	private String dataConsumption;
	
	private String usageFrequency;
	
	private String memoryUtilised;

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getNooftimeinstalled() {
		return nooftimeinstalled;
	}

	public void setNooftimeinstalled(String nooftimeinstalled) {
		this.nooftimeinstalled = nooftimeinstalled;
	}

	public String getDataConsumption() {
		return dataConsumption;
	}

	public void setDataConsumption(String dataConsumption) {
		this.dataConsumption = dataConsumption;
	}

	public String getUsageFrequency() {
		return usageFrequency;
	}

	public void setUsageFrequency(String usageFrequency) {
		this.usageFrequency = usageFrequency;
	}

	public String getMemoryUtilised() {
		return memoryUtilised;
	}

	public void setMemoryUtilised(String memoryUtilised) {
		this.memoryUtilised = memoryUtilised;
	}
	
	
	
	
	
	
	
}
